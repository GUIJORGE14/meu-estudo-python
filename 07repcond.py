# União de Conceitos

# Todas as estuturas de dados podem ser usadas separadamente mas
# em um programa "real", vamos unindo todas essas estururas para
# criarmos os cenários que precisamos para resolver um problema

# Exemplo:

# Você quer saber se uma palavra comtém a letra "Y".

palavra = "Guilherme"
letra_procurada = "i"

for letra in palavra:
    if letra == letra_procurada:
        print("A palavra contém a letra", letra_procurada)

    


    
